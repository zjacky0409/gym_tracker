package com.example.gym_tracker;

public class DataTesting {

    private String name;
    private int set;

    public DataTesting(String name, int set) {
        this.name = name;
        this.set = set;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSet () {
        return set;
    }

    public void setSet(int set) {
        this.set = set;
    }

}
